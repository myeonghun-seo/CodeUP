import java.util.*;

public class Main
{
	public static void main(String[] args)
	{
	    Scanner sc = new Scanner(System.in);
	    int a = sc.nextInt();
	    
	    if(a > 0)
	        System.out.println("plus");
	        
	    else
    	    System.out.println("minus");
    	    
	    // 나머지도 -로 나오니까 주의!
	    if(Math.abs(a % 2) == 1) 
	            System.out.println("odd");
	            
	        else 
	            System.out.println("even");
	    
	}

}
